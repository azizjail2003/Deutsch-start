// Per-Lektion flashcard decks for the Learn German Original A1 course.
//
// The lesson pages gate their content behind videos and downloadable PDF
// worksheets, so there is no transcript text to scrape cleanly. Instead, each
// deck below is authored to match exactly what its Lektion teaches — the same
// fixed A1 topics encoded in germanCourse.ts (greetings, numbers, articles…).
// Nouns carry their article and plural; most cards carry a short A1 example so
// the practice engine can build cloze and listening exercises from real usage.
//
// Card order roughly follows how a word is introduced in the lesson, so a
// learner who has watched Lektion 1–N practises only those words.

export type CardType =
  | "noun"
  | "verb"
  | "phrase"
  | "adjective"
  | "adverb"
  | "number"
  | "pronoun"
  | "question"
  | "article"
  | "other";

export type Flashcard = {
  de: string; // German. For nouns include the article, e.g. "der Tisch".
  en: string; // English meaning.
  type: CardType;
  plural?: string; // For nouns: full plural form, e.g. "die Tische".
  example?: string; // German example sentence.
  exampleEn?: string; // English of the example.
  note?: string; // Short usage hint.
};

export type GermanLevel = "A1" | "A2" | "B1";

export type LessonDeck = {
  level: GermanLevel;
  lesson: number; // Lesson number within its level.
  title: string; // Mirrors the lesson title.
  cards: Flashcard[];
};

