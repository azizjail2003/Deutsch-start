const withLevel = (decks: RawDeck[], level: GermanLevel): LessonDeck[] =>
  decks.map((deck) => ({ ...deck, level }));

// Ordered A1 → A2 → B1; a deck's position in this list is its curriculum index.
export const lessonDecks: LessonDeck[] = [
  ...withLevel(a1Decks, "A1"),
  ...withLevel(a2Decks, "A2"),
  ...withLevel(b1Decks, "B1"),
];

export type FlashcardWithMeta = Flashcard & {
  id: string;
  level: GermanLevel;
  lesson: number;
  lessonTitle: string;
  deckIndex: number; // 1-based position in the full curriculum order
};

/** Every card, tagged with a stable id, its level/lesson, and curriculum index. */
export const allCards: FlashcardWithMeta[] = lessonDecks.flatMap((deck, di) =>
  deck.cards.map((card, index) => ({
    ...card,
    id: `${deck.level}-${deck.lesson}-${index}`,
    level: deck.level,
    lesson: deck.lesson,
    lessonTitle: deck.title,
    deckIndex: di + 1,
  }))
);

/** Total number of decks across all levels (the curriculum length). */
export const totalDecks = lessonDecks.length;

/** Curriculum index (1-based) of a level+lesson, or 0 if not found. */
export function deckIndexOf(level: GermanLevel, lesson: number): number {
  const i = lessonDecks.findIndex((d) => d.level === level && d.lesson === lesson);
  return i < 0 ? 0 : i + 1;
}

/** All cards from the first `maxIndex` decks in curriculum order. */
export function cardsUpToIndex(maxIndex: number): FlashcardWithMeta[] {
  return allCards.filter((card) => card.deckIndex <= maxIndex);
}

/** Deck summaries up to maxIndex — handy for building level/lesson filters. */
export function decksUpToIndex(maxIndex: number): { level: GermanLevel; lesson: number; title: string; index: number }[] {
  return lessonDecks
    .map((d, i) => ({ level: d.level, lesson: d.lesson, title: d.title, index: i + 1 }))
    .filter((d) => d.index <= maxIndex);
}

/** The headword used for cloze/typing: nouns drop their article. */
export function headword(card: Flashcard): string {
  if (card.type === "noun") {
    const parts = card.de.split(" ");
    return parts.length > 1 ? parts.slice(1).join(" ") : card.de;
  }
  return card.de;
}

/** Article of a noun card (der/die/das), or null if not a simple noun. */
export function articleOf(card: Flashcard): "der" | "die" | "das" | null {
  if (card.type !== "noun") return null;
  const first = card.de.split(" ")[0];
  return first === "der" || first === "die" || first === "das" ? first : null;
}

function escapeRegExp(value: string): string {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

/**
 * Build a fill-the-gap from a card's example sentence by blanking its
 * headword, or null if the card has no usable example.
 */
export function clozeFor(card: Flashcard): { sentence: string; answer: string } | null {
  if (!card.example) return null;
  const hw = headword(card);
  if (!hw) return null;
  const re = new RegExp(`\\b${escapeRegExp(hw)}\\b`, "i");
  if (!re.test(card.example)) return null;
  return { sentence: card.example.replace(re, "_____"), answer: hw };
}
